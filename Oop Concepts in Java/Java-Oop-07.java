// -------- saves the memory ,, unlike objetss
class Student {

    String name;
    static String school;  // common for all.
    static public void change_School_name()
    {
        school = "Bilal Ashiq School";
    }

}

// ------------------------ Main Function ----------------------------
public class Oop7
{
    public static void main(String[] args) 
    {
        Student.school =  "ABC";  // will be same for all,
        Student s1 = new Student();
        s1.name = "Bila Jiii";
        s1.change_School_name();
        System.out.println("Name:   "+s1.name);
        System.out.println("School: "+s1.school);
    }
}
